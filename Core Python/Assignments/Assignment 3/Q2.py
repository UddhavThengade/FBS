alp = input('Enter The Elphabet : ')
if(alp=='a'or alp=='e'or alp=='i' or alp=='o' or alp=='u' or alp=='A' or alp=='E' or alp=='I' or alp=='O'or alp=='U'):
    print('This alphabet is the vowel.')
else:
    print('This alphabet is the consonant.')
